"""
Given a linked list of integers, remove any nodes from the linked list
	that have values that have previously occurred in the linked list.
Your function should return a reference to the head of the updated linked list.

Example:

Input: (3) -> (4) -> (3) -> (2) -> (6) -> (1) -> (2) -> (6) -> N
Output: (3) -> (4) -> (2) -> (6) -> (1) -> N
Explanation: The input list contains redundant nodes (3), (6), and (2),
	so those should be removed from the list.

[execution time limit] 4 seconds (py3)

[input] linkedlist.integer node
	The head node of the linked list.

[output] linkedlist.integer
	The head node of the updated linked list.
"""


# Singly-linked lists are already defined with this interface:
class Node:
	def __init__(self, value):
		self.value = value
		self.next = None


# Need to create the linked list
class LinkedList:
	def __init__(self, h=None):
		self.head = None
		self.tail = None

	def insert_node(self, value):
		new_node = Node(value)
		if self.head is None:
			self.head = new_node
			self.tail = self.head

		else:
			self.tail.next = new_node
			self.tail = new_node

	def print_list(self):
		curr = self.head

		while curr:
			print(curr.value)
			curr = curr.next


def new_list(node_list):
	linked = LinkedList()
	for i in range(len(node_list)):
		linked.insert_node(node_list[i])

	return linked.head


def condense_linked_list(node):
	"""
	Understanding:
	- Given the head node of a linked list
	- Need to traverse the linked list and omit any values that have already
	been seen.
	- Return the new linked list.
	"""
	# Create a new list to hold the values that are unique
	results = []

	# Traverse the linked list given
	while node:
		# If the value is not already in the new list...
		if node.value not in results:
			# need to add it to the new list
			results.append(node.value)

		# Current node points to next node
		node = node.next

	# Return the new list
	return results


